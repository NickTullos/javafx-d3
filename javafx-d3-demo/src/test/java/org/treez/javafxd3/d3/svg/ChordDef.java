package org.treez.javafxd3.d3.svg;

public class ChordDef {
	
	//#region ATTRIBUTES
	
	public double start;
	public double end;
	public double radius;

	//#end region
	
	//#region CONSTRUCTORS
	
	public ChordDef(double start, double end, double radius) {
		super();
		this.start = start;
		this.end = end;
		this.radius = radius;
	}
	
	//#end region

}
